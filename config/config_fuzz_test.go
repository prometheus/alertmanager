package config

import "testing"

func FuzzLoad(f *testing.F) {
	f.Add(`
global:
  resolve_timeout: 5m
route:
  group_by: ['alertname']
  group_wait: 10s
  group_interval: 10s
  repeat_interval: 1h
  receiver: 'web.hook'
receivers:
- name: 'web.hook'
  webhook_configs:
  - url: 'http://127.0.0.1:5001/'
`)

	f.Fuzz(func(t *testing.T, configText string) {
		_, _ = Load(configText)
	})
}
